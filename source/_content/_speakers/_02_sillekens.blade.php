	<h3>
		<em>Research on the physical sciences using ESA’s microgravity platforms</em>
	</h3>
</p>
<p class="ribbon__items news-item__body">
	Over the past decades, the European Space Agency (ESA) has developed and employed a suite of microgravity platforms that
	enables the scientific communities to conduct space-relevant investigations for the physical as well as for the life sciences.
	These platforms include drop towers, parabolic flights, sounding rockets, and the International Space Station (ISS), each
	with their particular features and hence application areas. This presentation will give an overview of the research on the
	physical sciences using ESA’s microgravity platforms, distinguishing between fundamental physics, astrobiology, soft and
	complex matter, two-phase heat transfer, and materials science. Particular emphasis will be on the materials- science related
	activities, with examples being on solidification studies (in which melt flow, sedimentation and floatation are effectively
	suppressed) by amongst others the use of X-ray imaging equipment, and the measurement of thermo-physical property data by
	means of an electromagnetic levitator. Results from these investigations provide for unique benchmark data for the testing
	and validation of fundamental theories and for modelling and simulation of (solidification) processes. This is to eventually
	lead to better predicting capabilities as to the formation of microstructures and defects in castings, the microstructural
	control for designed mechanical and physical properties, and so on.
</p>
<p class="ribbon__items news-item__body">
	Wim H. Sillekens (1963) is Materials Science Coordinator in the Science Department of the Directorate of Human Spaceflight
	and Robotic Exploration at the European Space Agency (ESA). As such he acts as a liaison between the scientific world and
	the agency for the preparation and execution of experiments using microgravity platforms. He obtained his Ph.D. from Eindhoven
	University of Technology, Netherlands, on a subject relating to metal-forming technology. Since he has been engaged in
	aluminium and magnesium research, amongst others on (hydro- mechanical) forming, recycling / refining, (hydrostatic) extrusion,
	forging, magnesium-based biodegradable implants, and more recently on melt-metallurgical processing of light-metal matrix
	nano-composites and grain-refined materials. His professional career includes positions as a post-doctoral researcher at
	his alma mater, as a research fellow at MEL (now AIST) in Tsukuba, Japan, and as a research scientist / project leader
	at the Netherlands Organization for Applied Scientific Research (TNO). In an earlier post at ESA, he acted as the coordinator
	of the European Community FP7 research project ExoMet. He has (co)-authored a variety of publications (about 150 entries
	to date), including patents, journal/proceedings papers, and conference presentations. Other professional activities include
	an involvement in association activities (for TMS and DGM), international conference committees, and as a peer reviewer
	of research papers and proposals. Research interests are in physical and mechanical metallurgy in general and in light-metals
	technology in particular.
</p>