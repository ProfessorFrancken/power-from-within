                <p><h3><em>Chair for the day</em></h3></p>
				<p class="ribbon__items news-item__body" >
					Erik Van der Giessen studied at and got his PhD degree from Delft University of Technology in 1987. During a subsequent research fellowship of the Royal Netherlands Academy of Sciences (KNAW), he spent postdoc periods at the Technical University in Denmark and at Brown University (Providence, USA) with the leaders in a research field that was germinating at that moment: micromechanics of materials. In 1992 he was appointed professor in this field in the departments of Mechanical Engineering and Materials Science at Delft University of Technology.                 </p>
                <p class="ribbon__items news-item__body" >
					As multiscale aspects of deformation and fracture became more essential in his line of research, moving his group to the Materials Science Center (now Zernike Institute for Advanced Materials) in Groningen at the start of 2001 was a splendid opportunity to incorporate chemophysical aspects. Also, this move opened the way to add biological materials and cells to the palette of materials under investigation.                 </p>
                <p class="ribbon__items news-item__body" >
					Erik Van der Giessen is a member of several societies and was elected member of the KNAW in 1999. He received various honours in the area of material mechanics, among which the W.T. Koiter Medal awarded by the American Society for Mechanical Engineering. From 2007 to 2013 he served as Deputy Director for (Applied) Physics teaching. From 2016, he serves as Editor-in-Chief of the scientific journal Modelling and Simulation in Materials Science and Engineering, published by the Institute of Physics (London).                </p>
